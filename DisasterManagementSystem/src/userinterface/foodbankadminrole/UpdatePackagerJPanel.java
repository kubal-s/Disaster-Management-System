    private void btnAddPackagerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPackagerActionPerformed
        // TODO add your handling code here:
        if(txtPackagerName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter packager name!");
            return;
        }
        if(txtPackagerUsername.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter packager username!");
            return;
        }
        for(UserAccount ua:this.ecosystem.getUserAccountDirectory().getUserAccountList()){
            if(ua.getUsername().equals(txtPackagerUsername.getText())&&!this.packager.getUsername().equals(txtPackagerUsername.getText())){
                JOptionPane.showMessageDialog(null,"Username already exists... please enter a different username!");
                return;
            }
        }
        if(txtPackagerPhoneNo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter packager phone Number!");
            return;
        }
        if(txtPackagerPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please enter packager password!");
            return;
        }
        for(UserAccount ua:this.ecosystem.getUserAccountDirectory().getUserAccountList()){
            if(ua.getUsername().equals(this.packager.getUsername())){
                ua.getUser().setName(txtPackagerName.getText());
                ua.getUser().setPhone(txtPackagerPhoneNo.getText());
                ua.setUsername(txtPackagerUsername.getText());
                ua.setPassword(txtPackagerPassword.getText());
                break;
            }
        }

        dB4OUtil.storeSystem(ecosystem);

        isEnabled(false);

        JOptionPane.showMessageDialog(null,"Packager updated successfully!");
        return;
        }
        public void initialize(){
            txtPackagerName.setText(this.packager.getUser().getName());
            txtPackagerPassword.setText(this.packager.getPassword());
            txtPackagerPhoneNo.setText(this.packager.getUser().getPhone());
            txtPackagerUsername.setText(this.packager.getUsername());
            outerloop:
            for(Network n:this.ecosystem.getNetworkList()){
                for(Enterprise e :n.getEnterpriseDirectory().getEnterpriseList()){
                    if(e.getUserAccount().getUsername().equals(this.foodBankAdminAccount.getUsername())){
                        this.currentEnterprise = e;
                        break outerloop;
                    }
                }
            }
    }//GEN-LAST:event_btnAddPackagerActionPerformed
